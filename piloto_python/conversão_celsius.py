#Coleta de dados
celsius  = int(input('Digite uma temperatura : '))

#Calculadora
f = (celsius  * 1.8 ) + 32
k = (celsius + 273)

#Resultado
print(f'a conversão de celsius para farenheit é : {f}')
print(f'a conversão de celsius para kelvin é : {k}')

